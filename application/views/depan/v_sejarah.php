                    <li class="list-group-item" text align="justify"><p>Sekolah SMA Muhammadiyah Sewon terletak di Dusun Mredo, Desa Bangunharjo, Kecamatan Sewon, Kabupaten Bantul, Daerah Istimewa Yogyakarta. Sekolah SMA Muhammadiyah Sewon berada jauh dari keramaian lalu lintas sekitar 300m sebelah barat dari Jalan Imogiri Barat dan sekitar 1km sebelah timur dari Jalan Parangtritis. Dengan demikian, suasana lingkungan sekolah menjadikan sekolah SMA Muhammadiyah Sewon berada di lingkungan yang nyaman dan kondusif terciptanya suasana belajar yang mendukung.
                    <p> Sekolah SMA Muhammadiyah Sewon merupakan salah satu sekolah swasta di wilayah Kabupaten bantul di bawah naungan Majelis Dikdasmen Muhammadiyah. Sekolah didirikan pada tahun 1985, yang saat itu kegiatan pembelajaran masih menumpang di salah satu rumah warga di daerah Salakan, Sewon. Pada tanggal 29 januari 1986 dikeluarkan piagam pendirian dari Perguruan Muhammadiyah, maka resmilah SMA Muhammadiyah Sewon sebagai salah satu perguruan Muhammadiyah milik persyarikatan Muhammadiyah dan berada di bawah pembinaan dan pengawasan Pimpinan Daerah Muhammadiyah Bantul.
                    <p>Pada tahun 1987, SMA Muhammadiyah Sewon pindah menempati gedung SD Muhammadiyah IV Karangkajen dengan KBM yang dilaksanakan pada sore hari. Meskipun dalam keadaan yang masih belum memiliki gedung tetap, jumlah peserta didik yang belajar di SMA Muhammadiyah Sewon mengalami peningkatan. Tahun 1990, dengan dikeluarkannya SK dirjen Dikdasmen Dikdasmen Dedikbud No. 009/Kep/I/U/1990 dan Surat Pemberitahuan Kanwil Dedikbud Provinsi DIY, SMA Muhammadiyah Sewon memperoleh akreditasi dengan predikat “Diakui”.
                    <p>SMA Muhammadiyah Sewon telah benar-benar memiliki kepercayaan baik dari Departemen pendidikan dan Kebudayaan maupun dari kalangan masyarakat. Pada tahun 1993, SMA Muhammadiyah Sewon mulai menempati gedung baru milik sendiri di Dusun Mredo, Bangunharjo, Sewon sampai saat ini."</li></p></p></p></p></div>
